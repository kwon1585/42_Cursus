#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	long long	n1;

	n1 = n;
	if (n < 0)
	{
		write(fd, "-", 1);
		n1 *= -1;
	}
	if (n1 >= 10)
		ft_putnbr_fd(n1 / 10, fd);
	c = n1 % 10 + 48;
	write(fd, &c, 1);
}
