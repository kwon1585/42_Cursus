char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	c = (unsigned char)c;
	while (*(s + i) == c || *(s + i))
	{
		if (*(s + i) == c)
			return ((char *)s + i);
		i++;
	}
	return (0);
}
